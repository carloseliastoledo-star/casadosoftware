<script setup lang="ts">
import { onMounted } from 'vue'

onMounted(() => {
  const homeDiv = document.getElementById('home-page')
  const privacyDiv = document.getElementById('privacy-page')
  const navHome = document.getElementById('nav-home-link')
  const navPrivacy = document.getElementById('nav-privacy-link')
  const footerPrivacy = document.getElementById('footer-privacy-link')
  const backHomeBtn = document.getElementById('back-to-home')

  function showHome() {
    if (homeDiv && privacyDiv) {
      homeDiv.classList.remove('hidden-page')
      privacyDiv.classList.add('hidden-page')
      window.history.pushState(null, '', '/')
      document.title = 'CPA Projetos | Contabilidade e Consultoria em São Francisco do Conde/BA'
    }
  }

  function showPrivacy() {
    if (homeDiv && privacyDiv) {
      homeDiv.classList.add('hidden-page')
      privacyDiv.classList.remove('hidden-page')
      window.history.pushState(null, '', '/politicas-de-privacidade')
      document.title = 'Política de Privacidade | CPA Projetos'
    }
  }

  function handleRoute() {
    const path = window.location.pathname
    if (path === '/politicas-de-privacidade') {
      showPrivacy()
    } else {
      showHome()
    }
  }

  if (navHome) {
    navHome.addEventListener('click', (e) => {
      e.preventDefault()
      showHome()
    })
  }
  
  if (navPrivacy) {
    navPrivacy.addEventListener('click', (e) => {
      e.preventDefault()
      showPrivacy()
    })
  }
  
  if (footerPrivacy) {
    footerPrivacy.addEventListener('click', (e) => {
      e.preventDefault()
      showPrivacy()
    })
  }
  
  if (backHomeBtn) {
    backHomeBtn.addEventListener('click', (e) => {
      e.preventDefault()
      showHome()
    })
  }

  window.addEventListener('popstate', handleRoute)
  handleRoute()
})
</script>

