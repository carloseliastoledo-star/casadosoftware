// Build reverse map: Unicode code point → CP850 byte (0x80..0xFF)
const UNICODE_TO_CP850 = new Map<number, number>()
for (let i = 0; i < CP850_EXT.length; i++) {
  UNICODE_TO_CP850.set(CP850_EXT[i], i + 0x80)
}

// Detect CP850 mojibake: box-drawing / block-element chars that shouldn't appear in normal text
const CP850_RE = /[\u2500-\u259F]/

export function fixCp850(value: unknown): string | null {
  if (typeof value !== 'string' || !value) return (value as string | null) ?? null
  if (!CP850_RE.test(value)) return value
  try {
    const bytes: number[] = []
    for (let i = 0; i < value.length; ) {
      const cp = value.codePointAt(i)!
      if (cp < 0x80) {
        bytes.push(cp)
        i += 1
      } else {
        const b = UNICODE_TO_CP850.get(cp)
        if (b !== undefined) {
          bytes.push(b)
        } else {
          // Char not in CP850: re-encode as UTF-8 bytes unchanged
          const enc = Buffer.from(String.fromCodePoint(cp), 'utf8')
          for (const byte of enc) bytes.push(byte)
        }
        i += cp > 0xFFFF ? 2 : 1
      }
    }
    return Buffer.from(bytes).toString('utf8')
  } catch {
    return value
  }
}
